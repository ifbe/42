#include<math.h>
double tangent(double in)
{
	return tan(in);
}
double arctan2(double y, double x)
{
	return atan2(y, x);
}


double sine(double in)
{
	return sin(in);
}
double arcsin(double x)
{
	return asin(x);
}


double cosine(double in)
{
	return cos(in);
}
double arccos(double x)
{
	return acos(x);
}


double squareroot(double in)
{
	return sqrt(in);
}
double exponent(double in)
{
	return exp(in);
}


double lg(double in)
{
	return log10(in);
}
double ln(double in)
{
	return log(in);
}
double log2(double in)
{
	return log(in)/log(2);
}