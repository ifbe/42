#include<math.h>
double sine(double in)
{
	return sin(in);
}
double cosine(double in)
{
	return cos(in);
}
double tangent(double in)
{
	return tan(in);
}




double squareroot(double in)
{
	return sqrt(in);
}




double exponent(double in)
{
	return exp(in);
}




double lg(double in)
{
	return log10(in);
}
double ln(double in)
{
	return log(in);
}
double log2(double in)
{
	return log(in)/log(2);
}